const DEFAULT_REPLY =
  "Я могу подсказать по нарядам, складу, PDF и тарифам. Опишите вопрос подробнее или нажмите «Позвать оператора», если нужна помощь человека.";

export function buildSupportBotReply(userText: string): string {
  const normalized = userText.trim().toLowerCase();
  if (!normalized) return DEFAULT_REPLY;

  for (const entry of FAQ) {
    if (entry.keywords.some((keyword) => normalized.includes(keyword))) {
      return entry.answer;
    }
  }

  if (/оператор|человек|менеджер|support|поддерж/.test(normalized)) {
    return "Нажмите «Позвать оператора» — мы получим уведомление и ответим в этом чате.";
  }

  return DEFAULT_REPLY;
}

